import react from "@vitejs/plugin-react";
import { defineConfig } from "vite";

const viteEnv = {};
Object.keys(process.env).forEach((key) => {
  if (key.startsWith(`VITE_`)) {
    viteEnv[`import.meta.env.${key}`] = process.env[key];
  }
});

export default defineConfig({
  define: viteEnv,
  plugins: [react()],
});
